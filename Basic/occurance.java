public class occurance {
    public static void main(String[] args){
        for(int i = 1; i<=100; i++){
            System.out.println(i); 
        }

        //while loop
        int j=100;
        while(j>=1){
            System.out.println(j);
            j = j-1;
        }

        //Do while loop
        int k = 100;
        do{
            System.out.println(k);
            k = k-1;
        } while(k>=1);
    }
    
}
